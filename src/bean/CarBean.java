public class CarBean {

    private int id;
    private String carThingName;
    private float carThingPrice;
    private int carThingNumber;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCarThingName() {
        return carThingName;
    }

    public void setCarThingName(String carThingName) {
        this.carThingName = carThingName;
    }

    public float getCarThingPrice() {
        return carThingPrice;
    }

    public void setCarThingPrice(float carThingPrice) {
        this.carThingPrice = carThingPrice;
    }

    public int getCarThingNumber() {
        return carThingNumber;
    }

    public void setCarThingNumber(int carThingNumber) {
        this.carThingNumber = carThingNumber;
    }

}
